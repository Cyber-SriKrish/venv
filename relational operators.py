#relational operators
a=float(input("Enter value of A:"))
b=float(input("Enter value of B:"))
print("="*50)
print("="*50)
print("{}>{} is {}".format(a,b,a>b))#greater than
print("="*50)
print("{}<{} is {}".format(a,b,a<b))#less than
print("="*50)
print("{}=={} is {}".format(a,b,a==b))#equality
print("="*50)
print("{}!={} is {}".format(a,b,a!=b))#not equals to
print("="*50)
print("{}>={} is {}".format(a,b,a>=b))#greater than or equals to
print("="*50)
print("{}<={} is {}".format(a,b,a<=b))#less than or equals to
print("="*50)
